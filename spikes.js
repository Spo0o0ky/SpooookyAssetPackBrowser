function getEntity(path) {
    // note: _id
    return `{
    {
        _fromLayer = "entities",
        _id = 165,
        _name = "spikesUp",
        _type = "entity",
        attachToSolid = true,
        type = "${path}",
        width = 24,
        x = 0,
        y = 0
    }
    }`
}

function buttonClick(button) {
    info = spikes[button.id.substring(7)]; // 7 is length of text "button_"
    navigator.clipboard.writeText(getEntity(info[1]));
    button.innerHTML = "Copied!";
    button.style.backgroundColor = "#e1bde0"
    setDefaultColor(button)
}

async function setDefaultColor(button) {
    await sleep(1000);
    button.innerHTML = "Copy";
    button.style.backgroundColor = null;
}

function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

for (let i = 0; i < spikes.length; i++) {
    info = spikes[i];
    //div for that spike
    let e = document.createElement("DIV-SPIKE");

    //title
    let title = document.createElement("DIV-NAME");
    title.innerHTML = info[0];

    //preview
    let image = document.createElement("IMG");
    image.setAttribute("src","./Images/spikes/" + info[0] + ".png");

    //"copy" button
    let button = document.createElement("BUTTON");
    button.innerHTML = "Copy";
    button.id = "button_" + i;
    button.setAttribute("onclick","buttonClick(this)");
        
    e.append(title);
    e.append(image);
    e.append(button);
    document.getElementById('main-content').appendChild(e);
}